package com.eot.banking.dto;

public class KwiksyProvider {
	
	private String provider_id;
	private String provider_name ;
	private String provider_description;
	private String  provider_image;
	
	
	public String getProvider_id() {
		return provider_id;
	}
	public void setProvider_id(String provider_id) {
		this.provider_id = provider_id;
	}
	public String getProvider_name() {
		return provider_name;
	}
	public void setProvider_name(String provider_name) {
		this.provider_name = provider_name;
	}
	public String getProvider_description() {
		return provider_description;
	}
	public void setProvider_description(String provider_description) {
		this.provider_description = provider_description;
	}
	public String getProvider_image() {
		return provider_image;
	}
	public void setProvider_image(String provider_image) {
		this.provider_image = provider_image;
	}
	
	

}
