package com.eot.banking.dto;

public class MobileDynamicGridMenu {
	

	  
	   private Integer gridMenuIndex;
	  
	   private String gridMenuTitle;
	  
	   private String gridMenuIconCode;
	  
	   private String gridMenuFunctionalCode;

	   public Integer getGridMenuIndex() {
	       return gridMenuIndex;
	   }

	   public void setGridMenuIndex(Integer gridMenuIndex) {
	       this.gridMenuIndex = gridMenuIndex;
	   }

	   public String getGridMenuTitle() {
	       return gridMenuTitle;
	   }

	   public void setGridMenuTitle(String gridMenuTitle) {
	       this.gridMenuTitle = gridMenuTitle;
	   }

	   public String getGridMenuIconCode() {
	       return gridMenuIconCode;
	   }

	   public void setGridMenuIconCode(String gridMenuIconCode) {
	       this.gridMenuIconCode = gridMenuIconCode;
	   }

	   public String getGridMenuFunctionalCode() {
	       return gridMenuFunctionalCode;
	   }

	   public void setGridMenuFunctionalCode(String gridMenuFunctionalCode) {
	       this.gridMenuFunctionalCode = gridMenuFunctionalCode;
	   }


}
